package loop_Demo;

public class While_vs_Do_while_Loop {
	public static void main(String[] args) {
		
	
        int p = 6;
        while(p <= 5){
            System.out.println("loop");
            p++;
        }
        System.out.println();
        

        // condition এর মান =5 ধরা হয়েছে, এবং variable এর মান =6 ধরা হয়েছে।।
        // variable এর মান =6 ধরার কারণে এ function টি print হয় নাই।। 
		// "while loop" function-এ condition থেকে variable এর মান বেশি হলে, print-এ কোন result আসবে না।।
        
        
        

        
	}

}
